            String sql = "SELECT * FROM mahasiswa WHERE nim LIKE ? OR nama LIKE ?";
            PreparedStatement st = conn.prepareStatement(sql);
                st.setString(1, "%" + cari + "%");
                st.setString(2, "%" + cari + "%");
