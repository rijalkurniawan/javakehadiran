    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        String nim = t_nim.getText();
        String nama = t_nama.getText();
        
        if (nim.isEmpty() || nama.isEmpty()){
            JOptionPane.showMessageDialog(this, "Semua Kolom Harus Diisi!", "Validasi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            String sql = "INSERT INTO mahasiswa (nim, nama) VALUES (?,?)";
            PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, nim);
            st.setString(2, nama);
            
            int rowInserted = st.executeUpdate();
            if (rowInserted > 0){
                JOptionPane.showMessageDialog(this, "Data Berhasil DItambahkan");
                resetForm();
                getData();
                
            }
            st.close();
        }catch (Exception e){
             Logger.getLogger(FormMahasiswa.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

    private void tbl_dataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_dataMouseClicked
        int selectedRow = tbl_data.getSelectedRow();
        if (selectedRow != -1){
            String nim = tbl_data.getValueAt(selectedRow, 1).toString();
            String nama = tbl_data.getValueAt(selectedRow, 2).toString();
            
            t_nim.setText(nim);
            t_nama.setText(nama);
        }
        btn_perbarui.setEnabled(true);
        btn_tambah.setEnabled(false);
        btn_hapus.setEnabled(true);
    }//GEN-LAST:event_tbl_dataMouseClicked

    private void btn_perbaruiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_perbaruiActionPerformed
        int selectedRow = tbl_data.getSelectedRow();
        if (selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Pilih Baris Yang Akan Diperbaharui");
            return;
        }
            String id = tbl_data.getValueAt(selectedRow, 0).toString();
            String nim = t_nim.getText();
            String nama = t_nama.getText();
            
            if (nim.isEmpty() || nama.isEmpty()){
            JOptionPane.showMessageDialog(this, "Semua Kolom Harus Diisi!", "Validasi", JOptionPane.ERROR_MESSAGE);
            return;
            }
            try {
                String sql = "UPDATE mahasiswa SET nim=?, nama=? WHERE id=?";
                PreparedStatement st = conn.prepareStatement(sql);
                st.setString(1, nim);
                st.setString(2, nama);
                st.setString(3, id);
            
                int rowUpdated = st.executeUpdate();
                if (rowUpdated > 0){
                    JOptionPane.showMessageDialog(this, "Data Berhasil Diperbaharui");
                    resetForm();
                    getData();
                
                }
                st.close();
                }catch (Exception e){
                Logger.getLogger(FormMahasiswa.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btn_perbaruiActionPerformed

